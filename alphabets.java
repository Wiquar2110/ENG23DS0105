public class alphabets {
    public static void main(String[] args) {
        System.out.println("uppercase alphabets:");
        for(int i=65;i<=90;i++){
            System.out.println((char)i+ "  ");
        }
        System.out.println("lowercase alphabets:");
        for(int i=97;i<=122;i++){
            System.out.println((char)i+ " ");
        }
    }
    
}
